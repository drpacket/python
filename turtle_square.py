import turtle

def drawSquare():
    """Draws a perfect square of 100*100
    pixels size"""
    window=turtle.Screen()
    window.bgcolor("red")
    brad=turtle.Turtle()
    brad.forward(100)
    brad.right(90)
    brade.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    brad.forward(100)
    brad.right(90)
    
    window.exitonclick()

